import pandas as pd
import duckdb


def read_preME_excel_data(file_path, peak):
    try:
        # Read the specified range from the Excel file
        df = pd.read_excel(
            file_path, 
            sheet_name=f'cordon_{peak}', 
            usecols='A:E',  # Columns A through E
        )

        df['peak'] = peak


        return df[['origin', 'destination', 'peak', 'time', 'vehicle', 'trips']]

    except Exception as e:
        print(f"Error reading Excel file: {e}")
        return None
    
